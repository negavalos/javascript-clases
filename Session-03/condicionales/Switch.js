// setencias Switch
let nota = 8;

switch (nota) {
    case 5:
        console.log("buen trabajo, sobresaliente!");
        break;
    case 4:
        console.log("buen trabajo, estuviste cerca de un sobresaliente!");
        break;
    case 3:
        console.log("has aprobado por poco");
        break;
    case 2:
        console.log("has reprobado");
        break;
    case 1:
        console.log("Reprobado");
        break;
    default:
        console.log("Error");
        break;
   
}